
import math

print( sum([1, 2, 3, 4]) )  # 求和
print( sum(range(1, 101)) )  # 求和

print( max([1, 3, 2]) )  # 最大值
print( max(1, 3, 2) )  # 最大值

print( min([1, -3, 2]) )  # 最小值
print( min(1, -3, 2) )  # 最小值

# print( pow(3, 4) )  # 3的4次方, 3**4

print( abs(-3) )  # 绝对值

print( round(34.567) )  # 35 四舍五入
print( round(34.567, 2) )  # 34.57 四舍五入, 保留2位小数

print(math.sqrt(81))  # 9 开平方根
print(math.ceil(3.1))  # 4 向上取整
print(math.floor(3.1))  # 3 向下取整
print(math.pi)  # π  3.141592653589793

