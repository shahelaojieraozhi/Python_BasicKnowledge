
# 赋值运算符
#   =
# 复合运算符:
#   +=  -= *=  /=  %=  **=  //=

a = 100

a = a + 1
print(a)  # 101

a += 1  # 自加1, 等价于: a=a+1
print(a)  # 102

a *= 2  # a = a * 2
print(a)  # 204

# 其他语言: a++, ++a, a--, --a


# 扩展: 了解
# 位运算符
print(12 & 5)  # 位与
#   12 => 1100
#   5  => 0101
# --------------
#   4  => 0100

print(12 | 5)  # 位或
#   12 => 1100
#   5  => 0101
# --------------
#   13 => 1101

print(12 ^ 5)  # 位异或,相同为0,不同为1
#   12 => 1100
#   5  => 0101
# --------------
#   9  => 1001

print(13 >> 2)  # 位右移2位
# 1101
#   11 01
#   11

print(13 << 2)  # 位左移2位
#    1101
#  110100

print(~6)  # -7, 按位取反

# 了解
# 原码,反码,补码
#   计算机内部使用补码进行运算
# 正数的原码,反码,补码是相同的.
# 负数的补码是反码+1

# 第1位是符号位, 0表示正数,1表示负数
# 00000000 00000000 00000000 00000110  =>  6
# 11111111 11111111 11111111 11111001  => ~6 补码
# 11111111 11111111 11111111 11111000  => 反码
# 10000000 00000000 00000000 00000111  => 原码 -7

