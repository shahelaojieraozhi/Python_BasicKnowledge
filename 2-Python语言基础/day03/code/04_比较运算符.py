
# 比较运算符: 关系运算符,会得到一个bool类型:True/False
#   >  >=  <  <=  ==  !=
print(10 > 4)  # True
print(10 >= 4)  # True
print(10 < 4)  # False
print(10 <= 4)  # False
print(10 == 4)  # False
print(10 != 4)  # True

# 字符串的比较
#   ASCII码比较
#   a~z : 97~122
#   A~Z : 65~90
#   0~9 : 48~57

# print(10 == '10')  # False
print('a' < 'b')  # True
print('a' < 'A')  # False
print('5' > 'A')  # False
print('abc' < 'acb')  # True
print('acd' > 'abcd')  # True
print('ab' > 'abc')  # False


