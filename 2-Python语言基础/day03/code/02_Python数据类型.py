
# Python数据类型
#   Number数值类型: int整数,float浮点型,complex复数
#   String字符串类型: str
#   Boolean布尔类型: bool, 有2个值: True,False
#   NoneType空类型: NoneType, 有1个值: None
#
#   list列表类型/数组: list
#   tuple元组类型: tuple
#   dict字典类型: dict
#   set集合类型: set (了解)
#   bytes字节类型/二进制类型: bytes

a = 10  # <class 'int'>
a = 10.3  # <class 'float'>
a = 'hello 鹿晗'  # <class 'str'>
a = True  # <class 'bool'>
a = None  # <class 'NoneType'>

a = [1, 2, 3]  # <class 'list'>
a = (1, 2, 3)  # <class 'tuple'>
a = {1, 2, 3}  # <class 'set'>
a = {'name': '鹿晗', 'movie': '上海堡垒'}  # <class 'dict'>
a = b'hello'  # <class 'bytes'>

print(type(a))



