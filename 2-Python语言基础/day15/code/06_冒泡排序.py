'''
冒泡排序
    
            l = [8, 7, 4, 9, 5, 6, 3, 2, 1]
                                                             i      j
    第1趟:  [8,7,4,9,5,6,3,2,1]  => [7,4,8,5,6,3,2,1, 9]      0     0~7  
    第2趟:  [7,4,8,5,6,3,2,1, 9] => [4,7,5,6,3,2,1, 8,9]      1     0~6
    第3趟:  [4,7,5,6,3,2,1, 8,9] => [4,5,6,3,2,1, 7,8,9]      2     0~5
    第4趟:  [4,5,6,3,2,1, 7,8,9] => [4,5,3,2,1, 6,7,8,9]      3     0~4
    第5趟:  [4,5,3,2,1, 6,7,8,9] => [4,3,2,1, 5,6,7,8,9]      4     0~3
    第6趟:  [4,3,2,1, 5,6,7,8,9] => [3,2,1, 4,5,6,7,8,9]      5     0~2
    第7趟:  [3,2,1, 4,5,6,7,8,9] => [2,1, 3,4,5,6,7,8,9]      6     0~1
    第8趟:  [2,1, 3,4,5,6,7,8,9] => [1, 2,3,4,5,6,7,8,9]      7     0
                                                       j ∈ range(len(l)-1 - i)
                                                       j ∈ range(8 - i)
'''
'''
l = [8, 7, 4, 9, 5, 6, 3, 2, 1]

for i in range(len(l)-1):  # i = 0,1,2,3,4,5,6,7 遍历的是趟数
    for j in range(len(l)-1-i):
        if l[j] > l[j+1]:
            l[j], l[j+1] = l[j+1], l[j]
print(l)
'''
# 练习
# 用冒泡排序对下面的列表排序，按照age升序
l = [
    {"name": "刘一", "age": 30},
    {"name": "陈二", "age": 20},
    {"name": "张三", "age": 10},
    {"name": "李四", "age": 50},
    {"name": "王五", "age": 40},
]

for i in range(len(l)-1):
    for j in range(len(l)-1-i):
        if l[j]['age'] > l[j+1]['age']:
            l[j], l[j+1] = l[j+1], l[j]

print(l)
