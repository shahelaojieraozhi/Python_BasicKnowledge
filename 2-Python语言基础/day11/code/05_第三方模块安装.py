#   1.使用pip命令
#       查看版本: pip -V
#       安装: pip install requests  [-i https://pypi.douban.com/simple]
#       卸载: pip uninstall requests
#       查看所有模块:  pip list
#       查看新装的模块:  pip freeze
#       查看模块详情: pip show requests

#       pip freeze > requirements.txt  把当前环境下的所有模块导出到文件中
#       pip install -r requirements.txt  把文件中的所有包安装到当前环境中

