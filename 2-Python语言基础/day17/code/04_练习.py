import re

# 提示: 下面要使用完全匹配
# 匹配手机号: 长度11,必须1开头,都是数字
print(re.search('^1\d{10}$', '18622223333'))

# 匹配qq号： 5-11位, 第一位不能为0
print(re.search('^[1-9]\d{4,10}$', '34234234'))

# 匹配任意一个邮箱: 必须包含@,@前是数字字母下户线, @后必须有.
#   如：jack@163.com，11@qq.com, aaa@sina.com.cn
print(re.search('^\w+@\w+(\.\w+){1,2}$', 'aaa@sina.com.cn'))
# 转义：\

# 匹配身份证: 18位，最后一位可能是X
print(re.search('^\d{17}[0-9X]$', '33344420001122667X'))


# 邮政编码(共6位数字, 第一位不能为0)
print(re.search('^[1-9]\d{5}$', '518000'))


