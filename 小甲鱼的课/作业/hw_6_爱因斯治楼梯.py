x = 7
i = 1
flag = 0
while i <= 10000:  # 在x == 7-70000里面搜这个数
    if (x % 2 == 1) and (x % 3 == 2) and (x % 5 == 4) and (x % 6 == 5):
        flag = 1
    else:
        x = 7 * (i + 1)  # 根据题意，x一定是7的整数倍，所以每次乘以7
    i += 1
if flag == 1:
    print('谢雨婷走的阶梯数是：', x)
    print('谢雨婷居然会用程序解题了，哦耶！！！')
else:
    print('在程序限定的范围内找不到答案！')

