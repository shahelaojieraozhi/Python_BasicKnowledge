# 注意是大括号
#   {'耐克':'just do it'} 耐克是key（键），just do it 是值（value）
dict1 = {'耐克': 'just do it', '李宁': '一切皆有可能', '鱼C工作室': '让编程改变世界', '阿迪达斯': 'impossible is nothing'}
#   冒号前是Key，冒号后是对应的值
print('鱼C工作室的口号是：', dict1["鱼C工作室"])
# 鱼C工作室的口号是： 让编程改变世界

dict2 = {1: 'one', 2: 'two', 3: 'three'}
print(dict2[2])
# two
